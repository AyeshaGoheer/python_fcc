


# built dicts using dict() function
dict = dict()
dict["money"] = 34
dict ["day"] = 2
dict ['age'] = 28
print (dict)


# dict literals (constants)

dict = {"money" : 34 , "day" : 2 , "age" : 28 }
print(dict)

# empty dict

dict = {}
print(dict)

# many counters with dicts
# count how often we see something
dict = {"day1" : 1 , "day2" : 2 , "day3" : 3 }
print (dict)
